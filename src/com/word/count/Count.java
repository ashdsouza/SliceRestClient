package com.word.count;

public class Count {
	private Integer countExactMatch;
	private Integer countInFiles;
	public Integer getCountExactMatch() {
		return countExactMatch;
	}
	public void setCountExactMatch(Integer countExactMatch) {
		this.countExactMatch = countExactMatch;
	}
	public Integer getCountInFiles() {
		return countInFiles;
	}
	public void setCountInFiles(Integer countInFiles) {
		this.countInFiles = countInFiles;
	}
}
